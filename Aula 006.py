"""Aula 006 - Operações matemáticas
Operações matemáticas entre variáveis

>>> x = 10
>>> y = 5
>>> soma = x + y
>>>soma
15

Vale tanto para adição quanto subtração
"""
x = 10
y = 5
soma = x + y
sub = x - y
print("x =", x, "\ny =", y)
print("x + y =", soma)
print("x - y =", sub)

"""
multiplicação e subtração
Multiplicação = sinal de asterístico (*)
Potenciação = dois asterísticos (**)
"""
"""
Divisão real = sinal de barra (/)
Divisão inteira = duas barras (//)
Resto da divisão = sinal de porcentagem (%)
"""
mult = x*y
exp = x**y
div = x/y
div_int = x//y
rest = x%y
print("x vezes y =", mult)
print("x elevado à y", exp)
print("x divivido por y (divisão real) =", div)
print("x diviido por y (divisão inteira) =", div_int)
print("resto de x/y =", rest)

